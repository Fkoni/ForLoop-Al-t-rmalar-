public class C02_NestedForLoop {

		  
			public static void  main(String[] args) {    
				
			//Bu konuyu bir örnekle açıklayabiliriz, emlakçıyız, üç müşterimiz,
			//üç te evimiz var, her müşteriye ayrı ayrı üç evi de gezdiriyoruz.
				
				
			for ( int  i= 1 ;i<= 3 ;i++){  
				 
				for ( int  j= 1 ;j<= 3 ;j++){  
					
			        System.out.println(i+ " " +j);  
				}  
			} 

    
}
}

