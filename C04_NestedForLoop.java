package _Team7_ForLoop.yedek;

public class C04_NestedForLoop {

		  
		public static void  main(String[] args) {   
			/*
			* 
			* * 
			* * * 
			* * * * 
			* * * * * 	görseli oluşturacak kodu yazınız			
			*/
			
			for ( int  i= 1 ;i<= 5 ;i++){  
			for ( int  j= 1 ;j<=i;j++){  
			        System.out.print( "* " );  
			}  
			System.out.println(); 

			} 

    
}
}

